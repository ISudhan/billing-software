// Helper function to get text based on selected language
// Usage: getText(key, language) where language is 'en' or 'ta'
export const getText = (key, language = 'en') => {
  if (language === 'ta') {
    return labels[key] || key;
  }
  return key;
};

// Legacy helper function to get bilingual label (both languages)
// Used only in specific contexts where both languages must be shown
export const getBilingual = (key) => {
  const tamil = labels[key];
  if (tamil) {
    return `${key} / ${tamil}`;
  }
  return key;
};

// Helper to get just Tamil
export const getTamil = (key) => {
  return labels[key] || key;
};
